export default class City {
  constructor(
    name,
    region,
    country,
    time,
    temperature,
    condition,
    wind,
    humidity,
  ) {
    this.name = name;
    this.region = region;
    this.country = country;
    this.time = time;
    this.temperature = temperature;
    this.condition = condition;
    this.wind = wind;
    this.humidity = humidity;
  }
}
